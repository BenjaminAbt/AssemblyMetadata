    // ── Helpers ─────────────────────────────────────────────────────────────────────────────

    /// <summary>
    /// Creates a minimal in-memory C# compilation used to host the generator under test.
    /// Only <c>System.Private.CoreLib</c> (via <c>typeof(object)</c>) is referenced because
    /// the generated source only uses primitive types (<c>string</c>, <c>long</c>, <c>int</c>).
    /// </summary>
    private static CSharpCompilation CreateEmptyCompilation() =>
        CSharpCompilation.Create(
            assemblyName: "TestAssembly",
            syntaxTrees: [],
            references: [MetadataReference.CreateFromFile(typeof(object).Assembly.Location)],
            options: new CSharpCompilationOptions(OutputKind.DynamicallyLinkedLibrary));

    /// <summary>
    /// Runs the <see cref="AssemblyMetadataGenerator"/> against an empty compilation and
    /// returns the <see cref="GeneratorDriverRunResult"/> for inspection.
    /// Uses <see cref="TestContext.Current"/> cancellation token for responsive cancellation.
    /// </summary>
    private static GeneratorDriverRunResult RunGenerator()
    {
        CSharpGeneratorDriver driver = CSharpGeneratorDriver.Create(new AssemblyMetadataGenerator());
        return driver
            .RunGenerators(CreateEmptyCompilation(), TestContext.Current.CancellationToken)
            .GetRunResult();
    }

