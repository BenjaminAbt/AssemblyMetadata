    // ── Value semantics ─────────────────────────────────────────────────────────────────────

    /// <summary>
    /// Verifies that the emitted <c>BuildTimestamp</c> value is a valid ISO 8601 round-trip
    /// string parseable with the <c>"o"</c> format specifier.
    /// </summary>
    [Fact]
    public void Generator_BuildTimestamp_IsValidIso8601()
    {
        string source = RunGenerator().GeneratedTrees[0].ToString();
        Match match = BuildTimestampRegex().Match(source);
        Assert.True(match.Success, "BuildTimestamp value not found in generated source.");

        // ParseExact throws if the format is wrong - no explicit Assert needed
        DateTimeOffset parsed = DateTimeOffset.ParseExact(match.Groups["val"].Value, "o", null);
        Assert.Equal(TimeSpan.Zero, parsed.Offset); // generator must always emit UTC
    }

    /// <summary>
    /// Verifies that the emitted <c>BuildFileTimeUtc</c> value is a positive long, representing a
    /// valid Windows FileTime after the epoch 1601-01-01T00:00:00Z.
    /// </summary>
    [Fact]
    public void Generator_BuildFileTime_IsPositive()
    {
        string source = RunGenerator().GeneratedTrees[0].ToString();
        Match match = BuildFileTimeRegex().Match(source);
        Assert.True(match.Success, "BuildFileTimeUtc value not found in generated source.");

        long fileTime = long.Parse(match.Groups["val"].Value, CultureInfo.InvariantCulture);
        Assert.True(fileTime > 0);
    }

    /// <summary>
    /// Verifies that all eight emitted constants are internally consistent: the timestamp,
    /// FileTime, year, month, day, hour, minute, and second must all refer to the same instant.
    /// </summary>
    /// <remarks>
    /// This is the key correctness test for <c>BuildSource</c>: it confirms that every constant
    /// is derived from the same <see cref="DateTimeOffset"/> captured in a single call,
    /// with no off-by-one or formatting errors.
    /// </remarks>
    [Fact]
    public void Generator_BuildTimestamp_IsConsistentWithAllComponents()
    {
        // Run the generator once; extract all eight values from the same emitted source
        // to verify the internal consistency of the BuildSource method.
        string source = RunGenerator().GeneratedTrees[0].ToString();

        string ts = BuildTimestampRegex().Match(source).Groups["val"].Value;
        long ft = long.Parse(BuildFileTimeRegex().Match(source).Groups["val"].Value,
            CultureInfo.InvariantCulture);
        int year = int.Parse(BuildDateYearRegex().Match(source).Groups["val"].Value,
            CultureInfo.InvariantCulture);
        int month = int.Parse(BuildDateMonthRegex().Match(source).Groups["val"].Value,
            CultureInfo.InvariantCulture);
        int day = int.Parse(BuildDateDayRegex().Match(source).Groups["val"].Value,
            CultureInfo.InvariantCulture);
        int hour = int.Parse(BuildTimeHourRegex().Match(source).Groups["val"].Value,
            CultureInfo.InvariantCulture);
        int minute = int.Parse(BuildTimeMinuteRegex().Match(source).Groups["val"].Value,
            CultureInfo.InvariantCulture);
        int second = int.Parse(BuildTimeSecondRegex().Match(source).Groups["val"].Value,
            CultureInfo.InvariantCulture);

        DateTimeOffset buildOn = DateTimeOffset.ParseExact(ts, "o", null);

        Assert.Equal(ft, buildOn.ToFileTime());
        Assert.Equal(year, buildOn.Year);
        Assert.Equal(month, buildOn.Month);
        Assert.Equal(day, buildOn.Day);
        Assert.Equal(hour, buildOn.Hour);
        Assert.Equal(minute, buildOn.Minute);
        Assert.Equal(second, buildOn.Second);
    }

    // ── Compilation correctness ─────────────────────────────────────────────────────────────

    /// <summary>
    /// Verifies that the generated C# source compiles without any errors when added to a
    /// <see cref="CSharpCompilation"/>. This catches syntax mistakes or invalid identifiers
    /// that could be introduced by changes to the source-building logic.
    /// </summary>
    [Fact]
    public void Generator_GeneratedSource_ProducesNoCompilationErrors()
    {
        CSharpCompilation compilation = CreateEmptyCompilation();
        CSharpGeneratorDriver driver = CSharpGeneratorDriver.Create(new AssemblyMetadataGenerator());

        driver.RunGeneratorsAndUpdateCompilation(
            compilation,
            out Compilation updatedCompilation,
            out _,
            TestContext.Current.CancellationToken);

        System.Collections.Generic.IEnumerable<Diagnostic> errors = updatedCompilation
            .GetDiagnostics(TestContext.Current.CancellationToken)
            .Where(d => d.Severity == DiagnosticSeverity.Error);

        Assert.Empty(errors);
    }

